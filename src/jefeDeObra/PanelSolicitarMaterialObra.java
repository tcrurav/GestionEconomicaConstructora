    private void actualizarCampoCoste(){
        Material material=materiales.get(cboMaterial.getSelectedIndex());

        float precioUnitarioMaterial=material.getPrecio();
        float unidadesMaterial=Float.parseFloat(campoCantidad.getText());
        
        float resultadoCoste=precioUnitarioMaterial*unidadesMaterial;
       
        campoCoste.setText(String.valueOf(resultadoCoste));
    }
    
    
