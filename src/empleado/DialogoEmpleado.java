    public void llenar() {
        String titulos[] = {"ID", "Dni", "Nombre", "Apellidos"};
        modelo = new DefaultTableModel(null, titulos);

        String fila[] = new String[4];
        ArrayList<Empleado> empleados = new ArrayList<>();
        try {
            empleados = dba.PersonaDba.getEmpleados();
        } catch (SQLException ex) {
            Logger.getLogger(FrmObra.class.getName()).log(Level.SEVERE, null, ex);
        }

        for (Empleado empleado : empleados) {
            fila[0] = String.valueOf(empleado.getPK_ID());
            fila[1] = empleado.getDni().toString();
            fila[2] = empleado.getNombre().toString();
            fila[3] = empleado.getApellidos().toString();
            
            modelo.addRow(fila);
        }
        tblEmpleados.setModel(modelo);
    }
    
    
