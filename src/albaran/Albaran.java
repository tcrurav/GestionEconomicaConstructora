    public void llenar(){
        conn = MySQL.getConnection();
        String titulos[] = {"proveedor","empleado","factura","fecha"};
        String sql = "select * from albaran";
        modelo = new DefaultTableModel(null, titulos);
        
        try {
            sentencia = conn.createStatement();
            ResultSet rs = sentencia.executeQuery(sql);
            
            String fila[] = new String[4];
            
            while(rs.next()){
                fila[0] = rs.getString("proveedor");
                fila[1] = rs.getString("empleado");
                fila[2] = rs.getString("factura");
                fila[3] = rs.getString("fecha");
                
                modelo.addRow(fila);
            }
            
            tblAlbaran.setModel(modelo);
            
        } catch (SQLException ex) {
            Logger.getLogger(Albaran.class.getName()).log(Level.SEVERE, null, ex);
        }
 
    }
    
    public void nuevo(){
        String sql = "insert into albaran (proveedor, empleado, factura, fecha) values (?, ?, ?, ?)";
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, txtProveedor.getText());
            ps.setString(2, txtEmpleado.getText());
            ps.setString(3, txtFactura.getText());
            ps.setString(4, txtFecha.getText());
            int n = ps.executeUpdate();
            
            if(n>0){
                JOptionPane.showMessageDialog(null, "Datos Guardados Correctamente.");
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Se ha producido un Error. Error:" + ex.getMessage());
        }
        
    }
    
    public void modificar(){
        int fila = tblAlbaran.getSelectedRow();
        String identificador = (String)tblAlbaran.getValueAt(fila, 0);
        String sql = "update albaran set proveedor=?, empleado=?, factura=?, fecha=? where factura=" + identificador;
        PreparedStatement ps;
        try {
            ps = conn.prepareStatement(sql);
            ps.setString(1, txtProveedor.getText());
            ps.setString(2, txtEmpleado.getText());
            ps.setString(3, txtFactura.getText());
            ps.setString(4, txtFecha.getText());
            
            int n = ps.executeUpdate();
            
            if(n>0){
                JOptionPane.showMessageDialog(null, "Datos Modificados");
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }        
    }

    public void borrar(){
        int fila = tblAlbaran.getSelectedRow();
        String identificador = (String)tblAlbaran.getValueAt(fila, 0);
        String sql = "delete from albaran where factura=" + identificador;
        try {
            sentencia = conn.createStatement();
            int n = sentencia.executeUpdate(sql);
            if (n>0){
                JOptionPane.showMessageDialog(null, "Datos Borrados");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
        
        
    }

