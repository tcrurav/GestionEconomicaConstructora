    private void tblSolicitarMaterialObraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSolicitarMaterialObraMouseClicked
        if (evt.getButton() == 1) {
            int fila = tblSolicitarMaterialObra.getSelectedRow();
            MaterialParaObra materialParaObra=new MaterialParaObra();
            
            try {                 
                materialParaObra=dba.SolicitarMaterialDba.getMaterialParaObra(Integer.parseInt(String.valueOf(tblSolicitarMaterialObra.getValueAt(fila, 0))));

            } catch (SQLException ex) {
                Logger.getLogger(PanelSolicitarMaterialObra.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                cboObras.setSelectedItem(ObraDba.getObra(materialParaObra.obra.getPK_ID()).getDireccion());
                
                cboJefeObra.setSelectedItem(PersonaDba.getJefeDeObra(materialParaObra.jefeDeObra.getPK_ID()).getNombre());////          
                
                cboAdministrativoObra.setSelectedItem(PersonaDba.getAdministrativoObra(materialParaObra.administrativoObra.getPK_ID()).getNombre());
                
                campoFechaSolicitud.setDate(materialParaObra.getFechaSolicitud());
                campoFechaRecepcion.setDate(materialParaObra.getFechaRecepcion());
                
                campoCantidad.setText(String.valueOf(materialParaObra.getCantidadMaterial()));
                campoCoste.setText(String.valueOf(materialParaObra.getCoste()));
                
                cboMaterial.setSelectedItem(MaterialDba.getMaterial(materialParaObra.material.getPK_ID()).getDescripcion());
                
                habilitar();
                habilitarBotonesParaTabla();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "No se ha podido leer la información en la BD");
            }
        }
    }//GEN-LAST:event_tblSolicitarMaterialObraMouseClicked

