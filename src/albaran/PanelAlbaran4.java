    public void llenar() {
        String titulos[] = {"ID", "Fecha", "Direccion"};
        modelo = new DefaultTableModel(null, titulos);

        String fila[] = new String[3];
        ArrayList<Albaran> albaranes = new ArrayList<>();
        try {
            albaranes = dba.AlbaranDba.getAlbaran();
        } catch (SQLException ex) {
       //     Logger.getLogger(FrmObra.class.getName()).log(Level.SEVERE, null, ex);
        }

        for (Albaran albaran : albaranes) {
            fila[0] = String.valueOf(albaran.getPK_ID());
            fila[1] = albaran.getFecha().toString();
            fila[2] = albaran.getNumAlbaran();

            modelo.addRow(fila);
        }

        tblLineaAlbaran.setModel(modelo);
    }

    public void nuevo() {
        Albaran albaran = new Albaran();

        try {
            albaran.setFecha(jXDatePickerFecha.getDate());
            albaran.setNumAlbaran(txtnumeroAlbaran.getText());
            
            if (dba.AlbaranDba.insertAlbaran(albaran)) {
                JOptionPane.showMessageDialog(null, "Datos Guardados Correctamente.");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Se ha producido un Error. Error:" + ex.getMessage());
        }

    }

    public void modificar() {
        int fila = tblLineaAlbaran.getSelectedRow();
        String identificador = (String) tblLineaAlbaran.getValueAt(fila, 0);

        Albaran albaran = new Albaran();

        try {
            albaran.setPK_ID(Integer.parseInt(identificador));
            albaran.setFecha(jXDatePickerFecha.getDate());
            albaran.setNumAlbaran(txtnumeroAlbaran.getText());
            

            if (dba.AlbaranDba.updateAlbaran(albaran)) {
                JOptionPane.showMessageDialog(null, "Datos Modificados");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }

    }

    public void borrar() {
        int fila = tblLineaAlbaran.getSelectedRow();
        String identificador = (String) tblLineaAlbaran.getValueAt(fila, 0);

        try {

            if (dba.AlbaranDba.deleteAlbaran(Integer.valueOf(identificador))) {
                JOptionPane.showMessageDialog(null, "Datos Borrados");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }

        
    }
    
