    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (quedanVacios()) {   // Metodo siguiente
            JOptionPane.showMessageDialog(null, "Los campos con asterisco no pueden estar vac√≠os");
        } else {
            Empleado empleado = new Empleado();
            Categoria categoriaSeleccionada = new Categoria();
                
            
            
            for (Categoria categoria : categorias) {
                if (cboCategorias.getSelectedItem() == categoria.getNombre()) {
                    categoriaSeleccionada = categoria;
                }
            }
            try {
                if ("modificacion".equals(operacion)) {
                    int fila = Integer.parseInt (filaDeTabla);
                    String identificador = (String) tblEmpleados.getValueAt(fila, 0);
                    empleado.setPK_ID(Integer.parseInt(identificador));
                }
                
                empleado.setNombre(txtNombre.getText());
                empleado.setApellidos(txtApellidos.getText());
                empleado.setTelefono(txtTelefono.getText());
                empleado.setDni(txtDni.getText());
                empleado.setCategoria(categoriaSeleccionada);
                if ("".equals(txtUsuario.getText())) { empleado.setUsuario("--");
                } else {empleado.setUsuario(txtUsuario.getText());}
                if ("".equals(txtContrasena.getText())) { empleado.setContra("--");
                } else {empleado.setContra(txtContrasena.getText());}
                
            
                if ("alta".equals(operacion)) { 
                    if (dba.PersonaDba.insertEmpleado(empleado)) {
                        JOptionPane.showMessageDialog(null, "Datos guardados correctamente.");
                    }
                } else if ("modificacion".equals(operacion)) {
                    if (dba.PersonaDba.updateEmpleado(empleado)) {
                        JOptionPane.showMessageDialog(null, "Datos actualizados correctamente.");
                    }
                }
                limpiar();
                panelDatosDelEmpleado.setVisible(false);
                panelOperacionesRelativasAUnEmpleado.setVisible(true);
                        
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Se ha producido un Error. Error:" + ex.getMessage());
            }
        }

    }//GEN-LAST:event_btnConfirmarActionPerformed

    
    private boolean quedanVacios() {
        boolean quedanVacios = true;
        if (!"".equals(txtNombre.getText()) && !"".equals(txtApellidos.getText()) && !"".equals(txtTelefono.getText()) && !"".equals(txtDni.getText())   ) {
            quedanVacios = false;
        }
        return quedanVacios;
    }

    
