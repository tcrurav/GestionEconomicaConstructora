    private void tblAlbaranMouseClicked(java.awt.event.MouseEvent evt) {                                        
        if (evt.getButton()==1){           
                int fila = tblAlbaran.getSelectedRow();              
                String sql = "select * from albaran where factura=" + tblAlbaran.getValueAt(fila, 0);
                //JOptionPane.showMessageDialog(null, sql);
            try {
                sentencia = conn.createStatement();
                ResultSet rs = sentencia.executeQuery(sql);
                rs.next();
                
                txtProveedor.setText(rs.getString("proveedor"));
                txtEmpleado.setText(rs.getString("empleado"));
                txtFactura.setText(rs.getString("factura"));
                txtFecha.setText(rs.getString("fecha"));
                
                habilitar();
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se ha podido leer la información en la BD");
            }
        }
    }
