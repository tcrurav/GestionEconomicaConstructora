    private void tblLineaAlbaranMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLineaAlbaranMouseClicked
        if (evt.getButton() == 1) {
            int fila = tblLineaAlbaran.getSelectedRow();
            Albaran albaran = new Albaran();
            try {
                albaran = dba.AlbaranDba.getAlbaran(Integer.parseInt(String.valueOf(tblLineaAlbaran.getValueAt(fila, 0))));
            } catch (SQLException ex) {
                //Logger.getLogger(FrmObra.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                jXDatePickerFecha.setDate(albaran.getFecha());
                txtnumeroAlbaran.setText(albaran.getNumAlbaran());
                

                habilitar();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "No se ha podido leer la información en la BD");
            }
        }
    }//GEN-LAST:event_tblLineaAlbaranMouseClicked

